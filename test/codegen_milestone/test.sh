#!/bin/bash

CODEGEN=../bin/c_codegen
EMULATOR=qemu-mips
MIPS_GCC=mips-linux-gnu-gcc
MAIN=main.c

if [ $# -eq 0 ]; then
  for i in input/*.c; do
    FILENAME=$(basename $i .c);

    ###################################
    ## Compile everything using gcc to get a reference (native) version

    ${MIPS_GCC} -static ${MAIN} $i -o build/${FILENAME}.gccref
    ${EMULATOR} build/${FILENAME}.gccref > output/${FILENAME}.reference

    ###################################
    ## Compile the function using your compiler and link it with the main using gcc

    # TODO: C pre-processor?

    # Compile the main and the assembly generated by your compiler
    ${MIPS_GCC} -static ${MAIN} build/${FILENAME}.s -o build/${FILENAME}
  
    # Run the executable
    ${EMULATOR} build/${FILENAME} > output/${FILENAME}.got

    ##################################
    ## Compare the outputs
    diff output/${FILENAME}.got output/${FILENAME}.reference > output/${FILENAME}.diff;
    if [[ $? != 0 ]]; then
      echo "${FILENAME}, FAIL, see output/${FILENAME}.diff";
    else
      echo "${FILENAME}, pass";
    fi
  done
else
  FILENAME=$1

    ###################################
    ## Compile everything using gcc to get a reference (native) version
    ${MIPS_GCC} -static ${MAIN} input/${FILENAME}.c -o build/${FILENAME}.gccref
    ${EMULATOR} build/${FILENAME}.gccref > output/${FILENAME}.reference

    ###################################
    ## Compile the function using your compiler and link it with the main using gcc

    # TODO: C pre-processor?

    # Compile the main and the assembly generated by your compiler
    ${MIPS_GCC} -static ${MAIN} build/${FILENAME}.s -o build/${FILENAME}
  
    # Run the executable
    ${EMULATOR} build/${FILENAME} > output/${FILENAME}.got

    ##################################
    ## Compare the outputs
    diff output/${FILENAME}.got output/${FILENAME}.reference > output/${FILENAME}.diff;
    if [[ $? != 0 ]]; then
      echo "${FILENAME}, FAIL, see output/${FILENAME}.diff";
    else
      echo "${FILENAME}, pass";
    fi
fi


